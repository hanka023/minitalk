#include "ft_printf.h"
#include <stdarg.h>
#include <stdio.h>
#include <unistd.h>

int	ft_printstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (*s)
	{
		ft_putchar(*s);
		s++;
		i++;
	}
	return (i);
}
